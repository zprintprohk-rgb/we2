let totalApplied = 0;
for (const locale of TARGETS) {
  const t = T[locale];
  if (!t) {
    console.error(`No translation table for ${locale}`);
    continue;
  }
  const flat = flatten(loadJson(locale));
  let applied = 0;
  let skipped = 0;
  for (const [k, v] of Object.entries(t)) {
    if (!(k in en)) {
      console.warn(`  ! ${locale}: key not in en.json: ${k}`);
      skipped++;
      continue;
    }
    if (flat[k] === v) {
      // already translated, no change needed
      continue;
    }
    flat[k] = v;
    applied++;
  }
  if (applied > 0) {
    saveJson(locale, unflatten(flat));
  }
  totalApplied += applied;
  console.log(`✓ ${locale}: applied ${applied} translations, skipped ${skipped}`);
}

console.log(`\nTotal translations applied: ${totalApplied}`);
